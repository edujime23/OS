escape_codes : dict[str, Callable[..., Any]] = {}

HIDE_CURSOR = lambda: u"\033[?25l"
SHOW_CURSOR = lambda: u"\033[?25h"

def TEXT_BG(*args, **kwargs):
    """
    Returns the text with a background color specified by RGB values.

    Args:
        red (int): The red component of the RGB color.
        green (int): The green component of the RGB color.
        blue (int): The blue component of the RGB color.

    Returns:
        str: The text with the specified background color.
    """
    if len(args) >= 3:
        red = args.pop()
        green = args.pop()
        blue = args.pop()
    elif len(kwargs.keys()) >= 3:
        red = kwargs.get("red", 255)
        green = kwargs.get("green", 255)
        blue = kwargs.get("blue", 255)
    else:
        red, green, blue = 255
    return f"\033[48;2;{red};{green};{blue}m"

def move_cursor(rows : int, columns : int):
    return f"\033[{rows};{columns}H"

escape_codes["HIDE_CURSOR"] = HIDE_CURSOR
escape_codes["SHOW_CURSOR"] = SHOW_CURSOR
escape_codes["TEXT_BG"] = TEXT_BG
