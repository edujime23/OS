class Convertors(Color):
    @property
    def hex_format(self):
        return ('#{:02X}{:02X}{:02X}'.format(self.red, self.green, self.blue))

    @property
    def rgb_format(self):
        return (self.red, self.green, self.blue)

    @property
    def terminal_rgb_format(self):
        return (f"\033[38;2;{self.red};{self.green};{self.blue}m")

    @property
    def hsl_format(self):
        r = self.red / 255
        g = self.green / 255
        b = self.blue / 255

        cmax = max(r, g, b)
        cmin = min(r, g, b)
        delta = cmax - cmin

        if delta == 0:
            hue = 0
        elif cmax == r:
            hue = ((g - b) / delta) % 6
        elif cmax == g:
            hue = (b - r) / delta + 2
        else:
            hue = (r - g) / delta + 4

        hue = int(hue * 60)
        lightness = int((cmax + cmin) / 2 * 100)

        if delta == 0:
            saturation = 0
        else:
            saturation = int(delta / (1 - abs(2 * lightness / 100 - 1)) * 100)

        return (hue, saturation, lightness)

    @property
    def cmyk_format(self):
        c = 1 - (self.red / 255)
        m = 1 - (self.green / 255)
        y = 1 - (self.blue / 255)
        k = min(c, m, y)
        if k == 1:
            c = m = y = 0
        else:
            c = (c - k) / (1 - k)
            m = (m - k) / (1 - k)
            y = (y - k) / (1 - k)
        return (int(c * 100), int(m * 100), int(y * 100), int(k * 100))

    @property
    def hsv_format(self):
        r = self.red / 255
        g = self.green / 255
        b = self.blue / 255

        cmax = max(r, g, b)
        cmin = min(r, g, b)
        delta = cmax - cmin

        if delta == 0:
            hue = 0
        elif cmax == r:
            hue = ((g - b) / delta) % 6
        elif cmax == g:
            hue = (b - r) / delta + 2
        else:
            hue = (r - g) / delta + 4

        hue = int(hue * 60)
        saturation = int((cmax - cmin) / cmax * 100)
        value = int(cmax * 100)

        return (hue, saturation, value)

    @property
    def xyz_format(self):
        r = self.red / 255
        g = self.green / 255
        b = self.blue / 255
        r = ((r + 0.055) / 1.055) ** 2.4 if r > 0.04045 else r / 12.92
        g = ((g + 0.055) / 1.055) ** 2.4 if g > 0.04045 else g / 12.92
        b = ((b + 0.055) / 1.055) ** 2.4 if b > 0.04045 else b / 12.92
        x = r * 0.4124 + g * 0.3576 + b * 0.1805
        y = r * 0.2126 + g * 0.7152 + b * 0.0722
        z = r * 0.0193 + g * 0.1192 + b * 0.9505
        return (int(x * 100), int(y * 100), int(z * 100))

